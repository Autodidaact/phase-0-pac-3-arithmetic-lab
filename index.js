function add(x, y){
    return x + y;
}
function subtract(a, b){
    return a - b;
}
function multiply(m, n){
    return m * n;
}
function divide(x, y){
    return x/y;
}
function increment(n){
    return n+1;
}
function decrement(n){
    return n-1;
}
function makeInt(n){
    return parseInt(n, 10);
}
function preserveDecimal(n){
    return parseFloat(n);
}